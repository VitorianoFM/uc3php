<?php

// Operadores Atribuição

$x = 10; // Variável do tipo inteiro
$x *= 3; // $x = $x * 3
echo "X = ".$x."\n"; // 30

echo "X = " .($x /= 10)."\n\n"; // $x = $x / 10

// = atribuição
// += adição
// -= subtração
// *= multiplicação
// /= divisão
// %= módulo

$z = 10;
$z += 5; // $z = $z + 5
$z -= 5; // $z = $z - 5
$z *= 5; // $z = $z * 5
$z /= 5; // $z = $z / 5
$z %= 5; // $z = $z % 5
