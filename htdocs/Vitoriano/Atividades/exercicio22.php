<?php

// Operadores Aritiméticos

$numero1 = 3; // Variável do tipo inteiro
$numero2 = 9; // Variável do tipo inteiro

// Operador de soma
$soma = $numero1 + $numero2;
echo "Soma = ".$soma."\n"; // 12

// Operador de subtração
$subtracao = $numero1 - $numero2;
echo "Subtração = ".$subtracao."\n"; // -6

// Operador de multiplicação
$multiplicacao = $numero1 * $numero2;
echo "Multiplicação = ".$multiplicacao."\n"; // 27

// Operador de divisão
$divisao = $numero1 / $numero2; // 0.33333333333333
echo "Divisão = ".$divisao."\n"; // 0.33333333333333